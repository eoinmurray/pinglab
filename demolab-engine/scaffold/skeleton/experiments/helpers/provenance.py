"""Git-commit provenance + a reproducer script for experiment runners — the
runner-side twin of what tools' setup_run_dir writes (RULES §4.7, §4.3).

A tool stamps `_provenance` into its `config.json` and drops a `run.sh` into
`temp/<tool>/<cmd>/`; a tool-backed runner inherits both by copying the tool's
config. An *inline* runner (one that computes its own numbers instead of shelling
out to a tool) has no tool config to inherit from, so it calls `stamp()` to record
which code produced the run. Every runner — inline or tool-backed — calls
`write_run_sh()` to leave a reproducer next to its committed record.

    from helpers import provenance

    numbers = {"fit": {"config": provenance.stamp({"seed": 0}), "r_squared": 0.98}}
    (ARTIFACTS / "numbers.json").write_text(json.dumps(numbers, indent=2) + "\n")
    provenance.write_run_sh(ARTIFACTS)

The stamp is kept separate from the copy in `tools/*/tool.py` on purpose: the
tool↔runner firewall (§4.5) forbids a tool importing `experiments/`, and tools may
not even be Python (§1.4). Keep the two behaviourally identical.
"""
import shlex
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path


def run_provenance() -> dict:
    """Git commit + dirty flag + UTC timestamp for this run, so every committed
    result records which code produced it. Degrades gracefully outside a git repo
    (e.g. a degit'd copy): commit is null."""
    here = Path(__file__).resolve().parent

    def git(*args: str) -> str:
        try:
            r = subprocess.run(["git", *args], cwd=here, capture_output=True, text=True, timeout=5)
            return r.stdout.strip() if r.returncode == 0 else ""
        except Exception:
            return ""

    commit = git("rev-parse", "HEAD")
    return {
        "commit": commit or None,
        # dirty = uncommitted *code* (tools/runners), not regenerated outputs.
        # `:/` = repo-top magic pathspec, so this is correct regardless of the cwd
        # git runs in (here: experiments/helpers). Plain "tools"/"experiments" would
        # resolve relative to cwd and silently match nothing (dirty always False).
        "dirty": bool(git("status", "--porcelain", "--", ":/tools", ":/experiments")) if commit else False,
        "generated_at": datetime.now(timezone.utc).isoformat(timespec="seconds"),
    }


def stamp(config: dict) -> dict:
    """Return a copy of `config` with a fresh `_provenance` block, so an inline
    runner's numbers.json entry is indistinguishable from a tool-produced one to
    numbers-table / provenance-footer / DOCTOR. Does not mutate the input."""
    return {**config, "_provenance": run_provenance()}


def write_run_sh(dst_dir: Path) -> Path:
    """Write an executable `run.sh` into `dst_dir` that re-invokes this runner with
    the same arguments — the runner-side twin of the reproducer tools write into
    `temp/<tool>/<cmd>/` (§4.3), except this lands in the committed
    `artifacts/data/<id>/` record. Assumes invocation from the repo root (the same
    assumption the tool's run.sh makes)."""
    script = Path(sys.argv[0]).resolve()
    try:
        script_rel = script.relative_to(Path.cwd())
    except ValueError:
        script_rel = script
    argv = ["uv", "run", "python", shlex.quote(str(script_rel)), *(shlex.quote(a) for a in sys.argv[1:])]
    body = "#!/bin/sh\n"
    body += f"# Regenerated by {script_rel} — reruns this experiment with the same arguments.\n"
    body += f"exec {' '.join(argv)}\n"
    path = dst_dir / "run.sh"
    path.write_text(body)
    path.chmod(0o755)
    return path
